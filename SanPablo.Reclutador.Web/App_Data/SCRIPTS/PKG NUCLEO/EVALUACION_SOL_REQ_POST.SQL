COMMENT ON COLUMN RECLU_PERSO_EXAMEN.IDERECLUTAPERSONA    IS 'Identificador de tabla de reclutamiento persona';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.IDEEVALUACION        IS 'Identificador de tabla EVALUACION_SOLREQ';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.IDEEVALUACION        IS 'Identificador de tabla EVALUACION_SOLREQ';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.IDUSUARESPONS     IS 'Identificador del Rol del Responsable de la evaluacion';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.FECEVALUACION        IS 'Fecha en la que el postulante ha rendido el examen';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.HORAEVALUACION       IS 'Hora que rendira la evaluacion';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.NOTAFINAL            IS 'Nota final obtenida por la persona en el examen';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.ARCHIVO              IS 'datos del examen';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.COMENTARIORESUL      IS 'Comentario del resultado de la evaluacion';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.USRCREACION          IS 'Usuario que creó el registro';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.FECCREACION          IS 'Fecha de creación del registro';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.USRMODIFICA          IS 'Usuario que modificó el registro';

