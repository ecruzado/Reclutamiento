DROP TABLE RECLU_PERSO_EXAMEN CASCADE CONSTRAINTS;

CREATE TABLE RECLU_PERSO_EXAMEN
(
  IDERECLUPERSOEXAMEN  NUMBER(8), 
  IDERECLUTAPERSONA    NUMBER(8), 
  IDEEVALUACIONSOLREQ  NUMBER(8), 
  IDEEVALUACIONCARGO   NUMBER(8),
  IDROLRESPONSABLE     NUMBER(8,2), 
  FECEVALUACION        DATE, 
  HORAEVALUACION       DATE, 
  NOTAFINAL            NUMBER(2), 
  ARCHIVO              BFILE, 
  COMENTARIORESUL      VARCHAR2(255),
  ESTADOEVALUACION     VARCHAR(20), 
  USRCREACION          VARCHAR2(15), 
  FECCREACION          DATE, 
  USRMODIFICA          VARCHAR2(15), 
  FECMODIFICA          DATE 
)
;

COMMENT ON TABLE RECLU_PERSO_EXAMEN IS 'Tabla de los exámenes que ha rendido la persona que esta participando en el reclutamiento';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.IDERECLUPERSOEXAMEN  IS 'Identificador de la tabla reclutamiento persona examen';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.IDERECLUTAPERSONA    IS 'Identificador de tabla de reclutamiento persona';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.IDEEVALUACIONSOLREQ  IS 'Identificador de tabla EVALUACION_SOLREQ';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.IDROLRESPONSABLE     IS 'Identificador del Rol del Responsable de la evaluacion';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.FECEVALUACION        IS 'Fecha en la que el postulante ha rendido el examen';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.HORAEVALUACION       IS 'Hora que rendira la evaluacion';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.NOTAFINAL            IS 'Nota final obtenida por la persona en el examen';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.ARCHIVO              IS 'datos del examen';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.COMENTARIORESUL      IS 'Comentario del resultado de la evaluacion';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.USRCREACION          IS 'Usuario que creó el registro';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.FECCREACION          IS 'Fecha de creación del registro';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.USRMODIFICA          IS 'Usuario que modificó el registro';

COMMENT ON COLUMN RECLU_PERSO_EXAMEN.FECMODIFICA          IS 'Fecha de modificación del registro';


ALTER TABLE RECLU_PERSO_EXAMEN ADD CONSTRAINT POSTU_RECLU_EXAMEN_PK 
  PRIMARY KEY (IDERECLUPERSOEXAMEN) 
 USING INDEX 
;

ALTER TABLE RECLU_PERSO_EXAMEN ADD CONSTRAINT POSTU_RECLU_E_EVALUACION_SO_FK 
  FOREIGN KEY (IDEEVALUACIONSOLREQ) REFERENCES EVALUACION_SOLREQ (IDEEVALUACIONSOLREQ)
;

ALTER TABLE RECLU_PERSO_EXAMEN ADD CONSTRAINT RECLU_PERSO_E_EVALUACION_CA_FK 
  FOREIGN KEY (IDEEVALUACIONCARGO) REFERENCES EVALUACION_CARGO (IDEEVALUACIONCARGO)
;