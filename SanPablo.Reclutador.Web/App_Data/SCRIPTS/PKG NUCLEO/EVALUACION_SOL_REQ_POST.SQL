DROP TABLE RECLU_PERSO_EXAMEN CASCADE CONSTRAINTS;

CREATE TABLE RECLU_PERSO_EXAMEN
(
  IDERECLUPERSOEXAMEN  NUMBER(8), 
  IDERECLUTAPERSONA    NUMBER(8), 
  IDEEVALUACION        NUMBER(8), 
  TIPSOLICITUD         VARCHAR2(3),
  IDUSUARESPONS        NUMBER(8,2), 
  FECEVALUACION        DATE, 
  HORAEVALUACION       DATE, 
  NOTAFINAL            NUMBER(2), 
  ARCHIVO              BFILE, 
  COMENTARIORESUL      VARCHAR2(255),
  ESTADOEVALUACION     VARCHAR(20),
  TIPESTEVALUACION     VARCHAR(3), 
  USRCREACION          VARCHAR2(15), 
  FECCREACION          DATE, 
  USRMODIFICA          VARCHAR2(15), 
  FECMODIFICA          DATE 
)
;

