COMMENT ON COLUMN NIVELACADEMICO_SOLREQ.PUNTNIVESTUDIO       IS 'Puntaje nivel estudios';
COMMENT ON COLUMN NIVELACADEMICO_SOLREQ.USRCREACION          IS 'Usuario que creó el registro';
