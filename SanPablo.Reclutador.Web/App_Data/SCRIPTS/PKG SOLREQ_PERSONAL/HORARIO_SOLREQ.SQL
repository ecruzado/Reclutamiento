DROP TABLE HORARIO_SOLREQ CASCADE CONSTRAINTS;

CREATE TABLE HORARIO_SOLREQ
(
  IDEHORARIOSOLREQ   NUMBER(8), 
  IDESOLREQPERSONAL  NUMBER(8), 
  TIPHORARIO         VARCHAR2(3), 
  PUNTHORARIO        NUMBER(2), 
  ESTACTIVO          VARCHAR2(1),
  USRCREACION        VARCHAR2(15), 
  FECCREACION        DATE, 
  USRMODIFICA        VARCHAR2(15), 
  FECMODIFICA        DATE 
)
;

COMMENT ON TABLE HORARIO_SOLREQ IS 'Tabla de horarios por solicitud de requerimiento';
COMMENT ON COLUMN HORARIO_SOLREQ.IDEHORARIOSOLREQ   IS 'Identificador de horario por solicitud de requerimiento';
COMMENT ON COLUMN HORARIO_SOLREQ.IDESOLREQPERSONAL  IS 'Identificador del solicitud de requerimiento';
COMMENT ON COLUMN HORARIO_SOLREQ.TIPHORARIO         IS 'Código del horario';
COMMENT ON COLUMN HORARIO_SOLREQ.PUNTHORARIO        IS 'Puntaje del horario';
COMMENT ON COLUMN HORARIO_SOLREQ.USRCREACION        IS 'Usuario que creó el registro';
COMMENT ON COLUMN HORARIO_SOLREQ.FECCREACION        IS 'Fecha de creación del registro';
COMMENT ON COLUMN HORARIO_SOLREQ.USRMODIFICA        IS 'Usuario que modificó el registro';
COMMENT ON COLUMN HORARIO_SOLREQ.FECMODIFICA        IS 'Fecha de modificación del registro';

ALTER TABLE HORARIO_SOLREQ ADD CONSTRAINT HORARIO_SOLREQ_PK PRIMARY KEY (IDEHORARIOSOLREQ);

ALTER TABLE HORARIO_SOLREQ ADD CONSTRAINT HORARIO_SOLREQ_SOLREQPERS_FK FOREIGN KEY (IDESOLREQPERSONAL) REFERENCES SOLREQ_PERSONAL (IDESOLREQPERSONAL);
 
ALTER TABLE HORARIO_SOLREQ MODIFY IDEHORARIOSOLREQ CONSTRAINT HORARIO_IDEHORARIOCARGO_NN NOT NULL;

ALTER TABLE HORARIO_SOLREQ MODIFY IDECARGO  CONSTRAINT HORARIO_CARGO_IDECARGO_NN NOT NULL;

