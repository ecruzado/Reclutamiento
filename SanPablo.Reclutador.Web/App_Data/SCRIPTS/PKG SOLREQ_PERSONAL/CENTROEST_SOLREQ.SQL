DROP TABLE CENTROEST_SOLREQ CASCADE CONSTRAINTS;

CREATE TABLE CENTROEST_SOLREQ
(
  IDECENTESTSOLREQ    NUMBER(8), 
  IDESOLREQPERSONAL   NUMBER(8), 
  TIPCENESTU          VARCHAR2(3), 
  TIPNOMCENESTU       VARCHAR2(3), 
  PUNTACENTROEST      NUMBER(3), 
  ESTACTIVO           VARCHAR2(1),
  USRCREACION         VARCHAR2(15), 
  FECCREACION         DATE, 
  USRMODIFICA         VARCHAR2(15), 
  FECMODIFICA         DATE 
)
;

COMMENT ON TABLE CENTROEST_SOLREQ IS 'Tabla de centros de estudio por solicitud de requerimiento'
;
COMMENT ON COLUMN CENTROEST_SOLREQ.IDECENTESTSOLREQ      IS 'Identificador de centro de estudios por solicitud de requerimiento'
;
COMMENT ON COLUMN CENTROEST_SOLREQ.IDESOLREQPERSONAL     IS 'Identificador del solicitud de requerimiento'
;
COMMENT ON COLUMN CENTROEST_SOLREQ.TIPCENESTU            IS 'Código centro de estudios'
;
COMMENT ON COLUMN CENTROEST_SOLREQ.TIPNOMCENESTU         IS 'Codigo de nombre de centro de estudios'
;
COMMENT ON COLUMN CENTROEST_SOLREQ.PUNTACENTROEST        IS 'Puntaje asignado al centro de estudios de procedencia'
;
COMMENT ON COLUMN CENTROEST_SOLREQ.USRCREACION           IS 'Usuario que creó el registro'
;
COMMENT ON COLUMN CENTROEST_SOLREQ.FECCREACION           IS 'Fecha de creación del registro'
;
COMMENT ON COLUMN CENTROEST_SOLREQ.USRMODIFICA           IS 'Usuario que modificó el registro'
;
COMMENT ON COLUMN CENTROEST_SOLREQ.FECMODIFICA           IS 'Fecha de modificación del registro'
;

ALTER TABLE CENTROEST_SOLREQ ADD CONSTRAINT CENTROESTSOLREQ_PK PRIMARY KEY (IDECENTESTSOLREQ);

