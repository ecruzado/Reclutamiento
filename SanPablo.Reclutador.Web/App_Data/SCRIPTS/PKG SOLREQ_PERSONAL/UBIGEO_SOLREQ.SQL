CREATE TABLE UBIGEO_SOLREQ
(
  IDEUBIGEOSOLREQ     NUMBER(8), 
  IDESOLREQPERSONAL   NUMBER(8,2), 
  IDEUBIGEO           NUMBER(6,2), 
