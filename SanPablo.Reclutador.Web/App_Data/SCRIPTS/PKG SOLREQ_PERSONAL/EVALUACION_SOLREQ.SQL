;
COMMENT ON COLUMN EVALUACION_SOLREQ.NOTAMINEXAMEN        IS 'Nota mínima del examen'
;
COMMENT ON COLUMN EVALUACION_SOLREQ.TIPAREARESPON        IS 'Código del área responsable del examen'
;
COMMENT ON COLUMN EVALUACION_SOLREQ.PUNTEXAMEN           IS 'Puntaje del examen'
;
COMMENT ON COLUMN EVALUACION_SOLREQ.ESTACTIVO            IS 'Estado del Registro'
;
COMMENT ON COLUMN EVALUACION_SOLREQ.USRCREACION          IS 'Usuario que creó el registro'
;
COMMENT ON COLUMN EVALUACION_SOLREQ.FECCREACION          IS 'Fecha de creación del registro'
;
