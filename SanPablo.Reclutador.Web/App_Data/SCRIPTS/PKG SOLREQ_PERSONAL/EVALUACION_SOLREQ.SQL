CREATE TABLE EVALUACION_SOLREQ
(
  IDEEVALUACIONSOLREQ  NUMBER(8), 
  IDESOLREQPERSONAL    NUMBER(8), 
  IDEEXAMEVAL          NUMBER(8), 
  TIPEXAMEN            VARCHAR2(3), 
  NOTAMINEXAMEN        NUMBER(2), 
  TIPAREARESPON        VARCHAR2(3), 
  PUNTEXAMEN           NUMBER(2), 
  ESTACTIVO            VARCHAR(1),
  USRCREACION          VARCHAR2(15), 
  FECCREACION          DATE, 
  USRMODIFICA          VARCHAR2(15), 
  FECMODIFICA          DATE 
)
;

