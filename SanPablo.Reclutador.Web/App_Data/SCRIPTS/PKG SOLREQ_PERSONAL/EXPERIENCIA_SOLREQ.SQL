DROP TABLE EXPERIENCIA_SOLREQ CASCADE CONSTRAINTS;

CREATE TABLE EXPERIENCIA_SOLREQ
(
  IDEEXPSOLREQ       NUMBER(8), 
  IDESOLREQPERSONAL  NUMBER(8), 
  TIPEXPLABORAL      VARCHAR2(3), 
  CANTANHOEXP        NUMBER(2), 
  CANTMESESEXP       NUMBER(2),
  PUNTEXPERIENCIA    NUMBER(2), 
  ESTACTIVO          VARCHAR2(1),
  USRCREACION        VARCHAR2(15), 
  FECCREACION        DATE, 
  USRMODIFICA        VARCHAR2(15), 
  FECMODIFICA        DATE 
)
;

COMMENT ON TABLE EXPERIENCIA_SOLREQ                     IS 'Tabla de experiencia laboral por requerimiento de personal';
