COMMENT ON COLUMN EXPERIENCIA_SOLREQ.TIPEXPLABORAL      IS 'Código experiencia laboral';
COMMENT ON COLUMN EXPERIENCIA_SOLREQ.CANTANHOEXP        IS 'Cantidad de años de experiencia';
COMMENT ON COLUMN EXPERIENCIA_SOLREQ.PUNTEXPERIENCIA    IS 'Puntaje por experiencia';
COMMENT ON COLUMN EXPERIENCIA_SOLREQ.USRCREACION        IS 'Usuario que creó el registro';
COMMENT ON COLUMN EXPERIENCIA_SOLREQ.FECCREACION        IS 'Fecha de creación del registro';
COMMENT ON COLUMN EXPERIENCIA_SOLREQ.USRMODIFICA        IS 'Usuario que modificó el registro';
COMMENT ON COLUMN EXPERIENCIA_SOLREQ.FECMODIFICA        IS 'Fecha de modificación del registro';

