DROP TABLE DEPENDENCIA CASCADE CONSTRAINTS
;
CREATE TABLE DEPENDENCIA
(
  IDEDEPENDENCIA   NUMBER(8), 
  IDESEDE          NUMBER(8), 
  NOMDEPENDENCIA   VARCHAR2(50), 
  ESTACTIVO        VARCHAR2(1), 
  USRCREACION      VARCHAR2(15), 
  FECCREACION      DATE, 
  USRMODIFICACION  VARCHAR2(15), 
  FECMODIFICACION  DATE 
)
;

COMMENT ON TABLE DEPENDENCIA IS 'Tabla de dependencias'
;
COMMENT ON COLUMN DEPENDENCIA.IDEDEPENDENCIA   IS 'Identificador de tabla'
;
COMMENT ON COLUMN DEPENDENCIA.IDESEDE          IS 'Identificador de la sede'
;
COMMENT ON COLUMN DEPENDENCIA.NOMDEPENDENCIA   IS 'Descripcion de dependencia'
;
COMMENT ON COLUMN DEPENDENCIA.USRCREACION      IS 'Usuario Creacion del registro.'
;
COMMENT ON COLUMN DEPENDENCIA.FECCREACION      IS 'Fecha de creacion del registro.'
;
COMMENT ON COLUMN DEPENDENCIA.USRMODIFICACION  IS 'Usuario de modificacion del registro.'
;
COMMENT ON COLUMN DEPENDENCIA.FECMODIFICACION  IS 'Fecha de modificacion.'
;

ALTER TABLE DEPENDENCIA ADD CONSTRAINT DEPENDENCIA_PK PRIMARY KEY (IDEDEPENDENCIA);

ALTER TABLE DEPENDENCIA ADD CONSTRAINT DEPENDENCIA_SEDE_FK FOREIGN KEY (IDESEDE) REFERENCES SEDE (IDESEDE);
 
ALTER TABLE DEPENDENCIA MODIFY IDEDEPENDENCIA CONSTRAINT DEPENDENCIA_IDEDEPENDENCIA_NN NOT NULL;

ALTER TABLE DEPENDENCIA MODIFY IDESEDE CONSTRAINT DEPENDENCIA_IDESEDE_NN NOT NULL;

ALTER TABLE DEPENDENCIA MODIFY NOMDEPENDENCIA CONSTRAINT DEPENDENCIA_NOMDEPENDENCIA_NN NOT NULL;

