DROP TABLE COMPETENCIAS_CARGO CASCADE CONSTRAINTS
;

CREATE TABLE COMPETENCIAS_CARGO
(
  IDECOMPETENCIACARGO  NUMBER(8), 
  IDECARGO             NUMBER(8), 
  TIPCOMPETEN          VARCHAR2(3), 
  ESTACTIVO            VARCHAR2(1),
  USRCREACION          VARCHAR2(15), 
  FECCREACION          DATE, 
  USRMODIFICA          VARCHAR2(15), 
  FECMODIFICA          DATE 
)
;

COMMENT ON TABLE COMPETENCIAS_CARGO IS 'Tabla de competencias por cargo'
;
COMMENT ON COLUMN COMPETENCIAS_CARGO.IDECOMPETENCIACARGO  IS 'Identificador de competencias por cargo'
;
COMMENT ON COLUMN COMPETENCIAS_CARGO.IDECARGO             IS 'Identificador del cargo'
;
COMMENT ON COLUMN COMPETENCIAS_CARGO.TIPCOMPETEN          IS 'Código de la competencia'
;
COMMENT ON COLUMN COMPETENCIAS_CARGO.USRCREACION          IS 'Usuario que creó el registro'
;
COMMENT ON COLUMN COMPETENCIAS_CARGO.FECCREACION          IS 'Fecha de creación'
;
COMMENT ON COLUMN COMPETENCIAS_CARGO.USRMODIFICA          IS 'Usuario que modificó el registro'
;
COMMENT ON COLUMN COMPETENCIAS_CARGO.FECMODIFICA          IS 'Fecha de modificacion'
;

