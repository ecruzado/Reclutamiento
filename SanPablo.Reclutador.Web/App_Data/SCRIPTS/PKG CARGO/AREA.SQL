DROP TABLE AREA CASCADE CONSTRAINTS
;
CREATE TABLE AREA
(
  IDEAREA          NUMBER(8), 
  IDEDEPARTAMENTO  NUMBER(8), 
  NOMAREA          VARCHAR2(50),
  ESTACTIVO        VARCHAR2(1), 
  USRCREACION      VARCHAR2(15), 
  FECCREACION      DATE, 
  USRMODIFICACION  VARCHAR2(15), 
  FECMODIFICACION  DATE 
)
;

COMMENT ON TABLE AREA IS 'Tabla de áreas'
;
COMMENT ON COLUMN AREA.IDEAREA          IS 'Identificador de la tabla'
;
COMMENT ON COLUMN AREA.IDEDEPARTAMENTO  IS 'Identificador de la tabla dependencia (Departamento)'
;
COMMENT ON COLUMN AREA.NOMAREA          IS 'Nombre del Area'
;
COMMENT ON COLUMN AREA.USRCREACION      IS 'Usuario Creacion del registro.'
;
COMMENT ON COLUMN AREA.FECCREACION      IS 'Fecha de creacion del registro.'
;
COMMENT ON COLUMN AREA.USRMODIFICACION  IS 'Usuario de modificacion del registro.'
;
COMMENT ON COLUMN AREA.FECMODIFICACION  IS 'Fecha de modificacion.'
;

ALTER TABLE AREA ADD CONSTRAINT AREA_PK PRIMARY KEY (IDEAREA) USING INDEX;

ALTER TABLE AREA ADD CONSTRAINT AREA_DEPARTAMENTO_FK FOREIGN KEY (IDEDEPARTAMENTO) REFERENCES DEPARTAMENTO (IDEDEPARTAMENTO);

ALTER TABLE AREA MODIFY IDEAREA CONSTRAINT AREA_IDEAREA_NN NOT NULL;

ALTER TABLE AREA MODIFY IDEDEPARTAMENTO CONSTRAINT AREA_IDEDEPARTAMENTO_NN NOT NULL;

ALTER TABLE AREA MODIFY NOMAREA CONSTRAINT AREA_NOMAREA_NN NOT NULL;
