DROP TABLE DISCAPACIDAD_CARGO CASCADE CONSTRAINTS;

CREATE TABLE DISCAPACIDAD_CARGO
(
  IDEDISCAPACARGO  NUMBER(8), 
  IDECARGO         NUMBER(8), 
  TIPDISCAPA       VARCHAR2(3), 
  DESDISCAPA       VARCHAR2(50), 
  PUNTDISCAPA      NUMBER(2), 
  ESTACTIVO        VARCHAR2(1),  
  USRCREACION      VARCHAR2(15), 
  FECCREACION      DATE, 
  USRMODIFICA      VARCHAR2(15), 
  FECMODIFICA      DATE  
)
;

COMMENT ON TABLE DISCAPACIDAD_CARGO IS 'Tabla de discapacidades por cargo';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.IDEDISCAPACARGO  IS 'Identificador de discapacidad por cargo';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.IDECARGO         IS 'Identificador del cargo';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.TIPDISCAPA       IS 'Código del tipo de discapacidad';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.DESDISCAPA       IS 'Descripción del la discapacidad';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.PUNTDISCAPA      IS 'Puntaje de la discapacidad';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.ESTACTIVO        IS 'Indicador de estado del registro';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.USRCREACION      IS 'Usuario que creó el registro';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.FECCREACION      IS 'Fecha de creación del registro';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.USRMODIFICA      IS 'Usuario que modificó el registro';
COMMENT ON COLUMN DISCAPACIDAD_CARGO.FECMODIFICA      IS 'Fecha de modificación del registro';

ALTER TABLE DISCAPACIDAD_CARGO ADD CONSTRAINT DISCAPACIDAD_CARGO_PK PRIMARY KEY (IDEDISCAPACARGO);

ALTER TABLE DISCAPACIDAD_CARGO ADD CONSTRAINT DISCAPACIDAD_CARGO_CARGO_FK FOREIGN KEY (IDECARGO) REFERENCES CARGO (IDECARGO);
 
ALTER TABLE DISCAPACIDAD_CARGO MODIFY IDEDISCAPACARGO CONSTRAINT DISCAP_CARGO_IDEDISCAPCARGO_NN NOT NULL;

ALTER TABLE DISCAPACIDAD_CARGO MODIFY IDECARGO  CONSTRAINT DISCAPACIDAD_CARGO_IDECARGO_NN NOT NULL;

CREATE INDEX DISCAPACIDAD_CARGO_IDX_1 ON DISCAPACIDAD_CARGO(IDECARGO);