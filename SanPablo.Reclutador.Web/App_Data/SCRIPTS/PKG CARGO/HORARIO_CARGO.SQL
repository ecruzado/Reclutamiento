DROP TABLE HORARIO_CARGO CASCADE CONSTRAINTS;


CREATE TABLE HORARIO_CARGO
(
  IDEHORARIOCARGO  NUMBER(8), 
  IDESEDE          NUMBER(3), 
  IDECARGO         NUMBER(8), 
  TIPHORARIO       VARCHAR2(3), 
  PUNTHORARIO      NUMBER(2),
  ESTACTIVO        VARCHAR(2), 
  USRCREACION      VARCHAR2(15), 
  FECCREACION      DATE, 
  USRMODIFICA      VARCHAR2(15), 
  FECMODIFICA      DATE 
)
;

COMMENT ON TABLE HORARIO_CARGO IS 'Tabla de horarios por cargo';
COMMENT ON COLUMN HORARIO_CARGO.IDEHORARIOCARGO  IS 'Identificador de horario por cargo';
COMMENT ON COLUMN HORARIO_CARGO.IDESEDE          IS 'Identificador de la sede';
COMMENT ON COLUMN HORARIO_CARGO.IDECARGO         IS 'Identificador del cargo';
COMMENT ON COLUMN HORARIO_CARGO.TIPHORARIO       IS 'Código del horario';
COMMENT ON COLUMN HORARIO_CARGO.PUNTHORARIO      IS 'Puntaje del horario';
COMMENT ON COLUMN HORARIO_CARGO.ESTACTIVO        IS 'Estado del registro';
COMMENT ON COLUMN HORARIO_CARGO.USRCREACION      IS 'Usuario que creó el registro';
COMMENT ON COLUMN HORARIO_CARGO.FECCREACION      IS 'Fecha de creación del registro';
COMMENT ON COLUMN HORARIO_CARGO.USRMODIFICA      IS 'Usuario que modificó el registro';
COMMENT ON COLUMN HORARIO_CARGO.FECMODIFICA      IS 'Fecha de modificación del registro';

ALTER TABLE HORARIO_CARGO ADD CONSTRAINT HORARIO_CARGO_PK PRIMARY KEY (IDEHORARIOCARGO);

ALTER TABLE HORARIO_CARGO ADD CONSTRAINT HORARIO_CARGO_CARGO_FK FOREIGN KEY (IDECARGO) REFERENCES CARGO (IDECARGO);
 
ALTER TABLE HORARIO_CARGO MODIFY IDEHORARIOCARGO CONSTRAINT HORARIO_IDEHORARIOCARGO_NN NOT NULL;

ALTER TABLE HORARIO_CARGO MODIFY IDECARGO  CONSTRAINT HORARIO_CARGO_IDECARGO_NN NOT NULL;

