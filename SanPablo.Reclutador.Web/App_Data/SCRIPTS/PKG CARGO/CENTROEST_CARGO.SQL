DROP TABLE CENTROEST_CARGO CASCADE CONSTRAINTS;

CREATE TABLE CENTROEST_CARGO
(
  IDECENTESTCARGO  NUMBER(8), 
  IDECARGO         NUMBER(8), 
  TIPCENESTU       VARCHAR2(3), 
  TIPNOMCENESTU    VARCHAR2(3), 
  PUNTACENTROEST   NUMBER(3), 
  ESTACTIVO        VARCHAR2(1),
  USRCREACION      VARCHAR2(15), 
  FECCREACION      DATE, 
  USRMODIFICA      VARCHAR2(15), 
  FECMODIFICA      DATE 
)
;

COMMENT ON TABLE CENTROEST_CARGO IS 'Tabla de centros de estudio por cargo'
;
COMMENT ON COLUMN CENTROEST_CARGO.IDECENTESTCARGO  IS 'Identificador de centro de estudios por cargo'
;
COMMENT ON COLUMN CENTROEST_CARGO.IDECARGO         IS 'Identificador del cargo'
;
COMMENT ON COLUMN CENTROEST_CARGO.TIPCENESTU       IS 'Código centro de estudios'
;
COMMENT ON COLUMN CENTROEST_CARGO.TIPNOMCENESTU    IS 'Codigo de nombre de centro de estudios'
;
COMMENT ON COLUMN CENTROEST_CARGO.PUNTACENTROEST   IS 'Puntaje asignado al centro de estudios de procedencia'
;
COMMENT ON COLUMN CENTROEST_CARGO.USRCREACION      IS 'Usuario que creó el registro'
;
COMMENT ON COLUMN CENTROEST_CARGO.FECCREACION      IS 'Fecha de creación del registro'
;
COMMENT ON COLUMN CENTROEST_CARGO.USRMODIFICA      IS 'Usuario que modificó el registro'
;
COMMENT ON COLUMN CENTROEST_CARGO.FECMODIFICA      IS 'Fecha de modificación del registro'
;

ALTER TABLE CENTROEST_CARGO ADD CONSTRAINT CENTROEST_PK PRIMARY KEY (IDECENTESTCARGO);

ALTER TABLE CENTROEST_CARGO ADD CONSTRAINT CENTROEST_CARGO_CARGO_FK FOREIGN KEY (IDECARGO) REFERENCES CARGO (IDECARGO);
 
ALTER TABLE CENTROEST_CARGO MODIFY IDECENTESTCARGO CONSTRAINT CENTROEST_IDECENTESTCARGO_NN NOT NULL;

ALTER TABLE CENTROEST_CARGO MODIFY IDECARGO  CONSTRAINT CENTROEST_IDECARGO_NN NOT NULL;

CREATE INDEX CENTROEST_CARGO_IDX_1 ON CENTROEST_CARGO(IDECARGO);
