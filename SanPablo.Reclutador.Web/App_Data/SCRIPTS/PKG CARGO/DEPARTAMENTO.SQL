DROP TABLE DEPARTAMENTO CASCADE CONSTRAINTS
;
CREATE TABLE DEPARTAMENTO
(
  IDEDEPARTAMENTO  NUMBER(8), 
  IDEDEPENDENCIA   NUMBER(8), 
  NOMDEPARTAMENTO  VARCHAR2(50),
  ESTACTIVO        VARCHAR2(1), 
  USRCREACION      VARCHAR2(15), 
  FECCREACION      DATE, 
  USRMODIFICACION  VARCHAR2(15), 
  FECMODIFICACION  DATE 
)
;

COMMENT ON TABLE DEPARTAMENTO IS 'Tabla de departamentos'
;
COMMENT ON COLUMN DEPARTAMENTO.IDEDEPARTAMENTO  IS 'Identificador de tabla'
;
COMMENT ON COLUMN DEPARTAMENTO.IDEDEPENDENCIA   IS 'Identificador de la tabla dependencia'
;
COMMENT ON COLUMN DEPARTAMENTO.NOMDEPARTAMENTO  IS 'Nombre del departamento'
;
COMMENT ON COLUMN DEPARTAMENTO.USRCREACION      IS 'Usuario Creacion del registro.'
;
COMMENT ON COLUMN DEPARTAMENTO.FECCREACION      IS 'Fecha de creacion del registro.'
;
COMMENT ON COLUMN DEPARTAMENTO.USRMODIFICACION  IS 'Usuario de modificacion del registro.'
;
COMMENT ON COLUMN DEPARTAMENTO.FECMODIFICACION  IS 'Fecha de modificacion.'
;

ALTER TABLE DEPARTAMENTO ADD CONSTRAINT DEPARTAMENTO_PK PRIMARY KEY (IDEDEPARTAMENTO) USING INDEX;

