DROP TABLE NIVELACADEMICO_CARGO CASCADE CONSTRAINTS;

CREATE TABLE NIVELACADEMICO_CARGO
(
  IDENIVELACADECARGO  NUMBER(8), 
  IDECARGO            NUMBER(8), 
  TIPEDUCACION        VARCHAR2(3), 
  TIPAREAESTUDIO      VARCHAR2(3), 
  TIPNIVELCANZADO     VARCHAR2(3),
  CICLOSEMESTRE       NUMBER(2), 
  PUNTNIVESTUDIO      NUMBER(8), 
  ESTACTIVO           VARCHAR2(1),
  USRCREACION         VARCHAR2(15), 
  FECCREACION         DATE, 
  USRMODIFICA         VARCHAR2(15), 
  FECMODIFICA         DATE  
)
;

COMMENT ON TABLE NIVELACADEMICO_CARGO IS 'Tabla de nivel académico por cargo';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.IDENIVELACADECARGO  IS 'Identificador de nivel académico por cargo';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.IDECARGO            IS 'Identificador del cargo';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.TIPEDUCACION        IS 'Código tipo de estudios';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.TIPAREAESTUDIO      IS 'Código area de estudio';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.TIPNIVELCANZADO     IS 'Código nivel alcanzado';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.PUNTNIVESTUDIO      IS 'Puntaje nivel estudios';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.USRCREACION         IS 'Usuario que creó el registro';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.FECCREACION         IS 'Fecha de creación del registro';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.USRMODIFICA         IS 'Usuario que modificó el registro';
COMMENT ON COLUMN NIVELACADEMICO_CARGO.FECMODIFICA         IS 'Fecha de modificación del registro';


ALTER TABLE NIVELACADEMICO_CARGO ADD CONSTRAINT NIVELACADEMICO_CARGO_PK PRIMARY KEY (IDENIVELACADECARGO);

