DROP TABLE CHSPRP.EVALUACION_CARGO CASCADE CONSTRAINTS;

CREATE TABLE CHSPRP.EVALUACION_CARGO
(
  IDEEVALUACIONCARGO  NUMBER(8) CONSTRAINT EVAL_CARGO_IDEEVALCARGO_NN NOT NULL,
  IDECARGO            NUMBER(8) CONSTRAINT EVALUACION_CARGO_IDECARGO_NN NOT NULL,
  IDEEXAMEVAL         NUMBER(8),
  TIPEXAMEN           VARCHAR2(3 CHAR),
  NOTAMINEXAMEN       NUMBER(2),
  TIPAREARESPON       VARCHAR2(3 CHAR),
  PUNTEXAMEN          NUMBER(2),
  ESTACTIVO           VARCHAR2(1 CHAR),
  USRCREACION         VARCHAR2(15 CHAR),
  FECCREACION         DATE,
  USRMODIFICA         VARCHAR2(15 CHAR),
  FECMODIFICA         DATE
);

COMMENT ON TABLE CHSPRP.EVALUACION_CARGO IS 'Tabla de evaluaciones por cargo';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.IDEEVALUACIONCARGO IS 'Identificador de evaluación por cargo';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.IDECARGO IS 'Identificador del cargo';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.IDEEXAMEVAL IS 'Código del examen de evaluación (Tabla exámenes)';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.TIPEXAMEN IS 'Código del tipo de examen';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.NOTAMINEXAMEN IS 'Nota mínima del examen';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.TIPAREARESPON IS 'Código del área responsable del examen';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.PUNTEXAMEN IS 'Puntaje del examen';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.ESTACTIVO IS 'Estado del Registro';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.USRCREACION IS 'Usuario que creó el registro';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.FECCREACION IS 'Fecha de creación del registro';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.USRMODIFICA IS 'Usuario que modificó el registro';

COMMENT ON COLUMN CHSPRP.EVALUACION_CARGO.FECMODIFICA IS 'Fecha de modificación del registro';


