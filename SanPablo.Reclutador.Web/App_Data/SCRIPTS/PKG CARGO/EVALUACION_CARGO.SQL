DROP TABLE EVALUACION_CARGO CASCADE CONSTRAINTS;

CREATE TABLE EVALUACION_CARGO
(
  IDEEVALUACIONCARGO  NUMBER(8), 
  IDECARGO            NUMBER(8), 
  IDEEXAMEVAL         NUMBER(8), 
  TIPEXAMEN           VARCHAR2(3), 
  NOTAMINEXAMEN       NUMBER(2), 
  TIPAREARESPON       VARCHAR2(3), 
  PUNTEXAMEN          NUMBER(2), 
  ESTACTIVO           VARCHAR(1),
  USRCREACION         VARCHAR2(15), 
  FECCREACION         DATE, 
  USRMODIFICA         VARCHAR2(15), 
  FECMODIFICA         DATE 
)
;

COMMENT ON TABLE EVALUACION_CARGO IS 'Tabla de evaluaciones por cargo'
;
COMMENT ON COLUMN EVALUACION_CARGO.IDEEVALUACIONCARGO  IS 'Identificador de evaluación por cargo'
;
COMMENT ON COLUMN EVALUACION_CARGO.IDECARGO            IS 'Identificador del cargo'
;
COMMENT ON COLUMN EVALUACION_CARGO.IDEEXAMEVAL         IS 'Código del examen de evaluación (Tabla exámenes)'
;
COMMENT ON COLUMN EVALUACION_CARGO.TIPEXAMEN           IS 'Código del tipo de examen'
;
COMMENT ON COLUMN EVALUACION_CARGO.NOTAMINEXAMEN       IS 'Nota mínima del examen'
;
COMMENT ON COLUMN EVALUACION_CARGO.TIPAREARESPON       IS 'Código del área responsable del examen'
;
COMMENT ON COLUMN EVALUACION_CARGO.PUNTEXAMEN          IS 'Puntaje del examen'
;
COMMENT ON COLUMN EVALUACION_CARGO.ESTACTIVO           IS 'Estado del Registro'
;
COMMENT ON COLUMN EVALUACION_CARGO.USRCREACION         IS 'Usuario que creó el registro'
;
COMMENT ON COLUMN EVALUACION_CARGO.FECCREACION         IS 'Fecha de creación del registro'
;
COMMENT ON COLUMN EVALUACION_CARGO.USRMODIFICA         IS 'Usuario que modificó el registro'
;
COMMENT ON COLUMN EVALUACION_CARGO.FECMODIFICA         IS 'Fecha de modificación del registro'
;

