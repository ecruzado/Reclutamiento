DROP TABLE DISCAPACIDAD_POSTULANTE CASCADE CONSTRAINTS;

CREATE TABLE DISCAPACIDAD_POSTULANTE
(
  IDEDISCAPACIDADPOSTULANTE  NUMBER(8), 
  IDEPOSTULANTE              NUMBER(8), 
  TIPODISCAPACIDAD           VARCHAR2(3), 
  DESDISCAPACIDAD            VARCHAR2(50), 
  ESTACTIVO                  VARCHAR2(1), 
  USRCREACION                VARCHAR2(15), 
  FECCREACION                DATE, 
  USRMODIFICACION            VARCHAR2(15), 
  FECMODIFICACION            DATE 
)
;

