COMMENT ON COLUMN DISCAPACIDAD_POSTULANTE.IDEPOSTULANTE              IS 'Identificador de la persona';
COMMENT ON COLUMN DISCAPACIDAD_POSTULANTE.TIPODISCAPACIDAD           IS 'Código del tipo de discapacidad';
COMMENT ON COLUMN DISCAPACIDAD_POSTULANTE.DESDISCAPACIDAD            IS 'Descripción del la discapacidad';
COMMENT ON COLUMN DISCAPACIDAD_POSTULANTE.ESTACTIVO                  IS 'Indicador de estado A(activo) y I(inactivo)';
COMMENT ON COLUMN DISCAPACIDAD_POSTULANTE.USRCREACION                IS 'Usuario que creó el registro';
COMMENT ON COLUMN DISCAPACIDAD_POSTULANTE.FECCREACION                IS 'Fecha de creación del registro';
COMMENT ON COLUMN DISCAPACIDAD_POSTULANTE.USRMODIFICACION            IS 'Usuario que modificó el registro';
COMMENT ON COLUMN DISCAPACIDAD_POSTULANTE.FECMODIFICACION            IS 'Fecha de modificación del registro';


