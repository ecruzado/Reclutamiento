COMMENT ON COLUMN ESTUDIOS_POSTULANTE.FECESTUDIOINICIO       IS 'Fecha de inicio de estudios';
COMMENT ON COLUMN ESTUDIOS_POSTULANTE.FECESTUDIOFIN          IS 'Fecha de fin de estudios';
COMMENT ON COLUMN ESTUDIOS_POSTULANTE.INDESTACTUALMENTE      IS 'Indicador en caso se encuentre estudiando';
COMMENT ON COLUMN ESTUDIOS_POSTULANTE.ESTACTIVO              IS 'Indicador de estado A(activo) y I(inactivo)';
COMMENT ON COLUMN ESTUDIOS_POSTULANTE.USRCREACION            IS 'Usuario que creó el registro';
COMMENT ON COLUMN ESTUDIOS_POSTULANTE.FECCREACION            IS 'Fecha de creación del registro';
