CREATE TABLE ESTUDIOS_POSTULANTE
(
  IDEESTUDIOSPOSTULANTE  NUMBER(8), 
  IDEPOSTULANTE          NUMBER(8), 
  TIPTIPOINSTITUCION     VARCHAR2(3), 
  TIPNOMINSTITUCION      VARCHAR2(100),
  NOMINSTITUCION         VARCHAR2(100),
  TIPAREA                VARCHAR2(3), 
  TIPEDUCACION           VARCHAR2(3), 
  TIPNIVELALCANZADO      VARCHAR2(3), 
  FECESTUDIOINICIO       DATE, 
  FECESTUDIOFIN          DATE, 
  INDESTACTUALMENTE      VARCHAR2(1), 
  ESTACTIVO              VARCHAR2(1), 
  USRCREACION            VARCHAR2(15), 
  FECCREACION            DATE, 
  USRMODIFICACION        VARCHAR2(50), 
  FECMODIFICACION        DATE 
)
;

