CREATE TABLE GENERAL
(
	IDEGENERAL       NUMBER(4), 
	TIPTABLA         VARCHAR2(15), 
	DESCRIPCION      VARCHAR2(50), 
	TIPDATO          VARCHAR2(8),
	LONCAMPO         VARCHAR2(3),
	USRCREACION      VARCHAR2(15), 
	FECCREACION      DATE, 
	USRMODIFICACION  VARCHAR2(15), 
	FECMODIFICACION  DATE 
);

