CREATE TABLE CRITERIO
(
  IDECRITERIO      NUMBER(8), 
  TIPMEDICION      VARCHAR2(2), 
  TIPCRITERIO      VARCHAR2(2), 
  TIPMODO          VARCHAR2(2),
  PREGUNTA         VARCHAR2(400),
  TIPCALIFICACION  VARCHAR2(2),
  ORDENIMPRESION   NUMBER(4),
  ESTACTIVO        VARCHAR2(1), 
  USRCREACION      VARCHAR2(15), 
  FECCREACION      DATE, 
  USRMODIFICACION  VARCHAR2(15), 
  FECMODIFICACION  DATE 
);

