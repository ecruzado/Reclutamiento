COMMENT ON COLUMN CRITERIO.TIPCALIFICACION  IS 'Tipo de calificacion';
COMMENT ON COLUMN CRITERIO.ORDENIMPRESION   IS 'Orden de impresion';
COMMENT ON COLUMN CRITERIO.ESTACTIVO        IS 'Indicador de estado A(Activo), I(Inactivo)';
COMMENT ON COLUMN CRITERIO.USRCREACION      IS 'Usuario de creacion';
COMMENT ON COLUMN CRITERIO.FECCREACION      IS 'Fecha de creacion';
COMMENT ON COLUMN CRITERIO.USRMODIFICACION  IS 'Usuario de modificacion';
